package Basic_OOPs;


public class PersonClass {
    String name="Bhushan Kumar";
    int age = 22;
    public static void main(String[] args) {
        PersonClass obj = new PersonClass();
        System.out.println("Name: " + obj.name);
        System.out.println("Age: " + obj.age);
    }

}

